#!/usr/bin/env node
import { execFile } from "node:child_process";
import { promises as fs } from "node:fs";
import path from "node:path";
import { promisify } from "node:util";
import { OPENWIKI_ERROR_MODEL } from "@openwiki/core";
import { handleMcpRequest } from "@openwiki/mcp-server";
import {
  mcpToolModeOperations,
  operationNames,
  requiredScopesForOperation,
} from "@openwiki/policy";
import { mcpManifest, openApiDocument } from "@openwiki/static-export";

const execFileAsync = promisify(execFile);
const root = process.cwd();
const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");
const GENERATED = "<!-- This file is generated by `pnpm docs:reference`. Do not edit by hand. -->\n\n";

const docs = await generateReferenceDocs();
if (checkOnly) {
  await checkReferenceDocs(docs);
} else {
  await writeReferenceDocs(docs);
}

async function generateReferenceDocs() {
  const cliHelp = await openwikiHelp();
  const mcp = await mcpToolsByMode();
  const schemas = await schemaSummaries();
  const packages = await packageSummaries();
  const openapi = openApiDocument();
  const manifest = mcpManifest();
  return new Map([
    ["docs/reference/cli.md", renderCliReference(cliHelp)],
    ["docs/reference/mcp-tools.md", renderMcpReference(mcp, manifest)],
    ["docs/reference/operations.md", renderOperationsReference(mcp, openapi, manifest)],
    ["docs/reference/schemas.md", renderSchemaReference(schemas)],
    ["docs/reference/package-apis.md", renderPackageApiReference(packages)],
    ["docs/reference/errors.md", renderErrorReference()],
    ["docs/reference/compatibility.md", renderCompatibilityReference()],
  ]);
}

async function openwikiHelp() {
  const { stdout } = await execFileAsync(
    process.execPath,
    ["--no-warnings", "--import", "tsx", "packages/cli/src/main.ts", "--help"],
    { cwd: root, maxBuffer: 1024 * 1024 },
  );
  return stdout.trim();
}

async function mcpToolsByMode() {
  const modes = ["read", "proposal", "write"];
  const result = {};
  for (const mode of modes) {
    result[mode] = await mcpToolList(mode);
  }
  return result;
}

async function mcpToolList(mode) {
  const response = await handleMcpRequest(
    root,
    { jsonrpc: "2.0", id: mode, method: "tools/list" },
    { toolMode: mode },
  );
  if (!isRecord(response) || !Array.isArray(response.tools)) {
    throw new Error(`Expected MCP tools/list response for ${mode}`);
  }
  return response.tools.map((tool) => {
    if (!isRecord(tool) || typeof tool.name !== "string") {
      throw new Error(`Expected MCP tool definition in ${mode}`);
    }
    return tool;
  });
}

async function schemaSummaries() {
  const schemaDir = path.join(root, "schemas", "openwiki", "v0");
  const names = (await fs.readdir(schemaDir)).filter((name) => name.endsWith(".schema.json")).sort();
  return Promise.all(
    names.map(async (name) => {
      const schema = JSON.parse(await fs.readFile(path.join(schemaDir, name), "utf8"));
      return {
        file: `schemas/openwiki/v0/${name}`,
        id: typeof schema.$id === "string" ? schema.$id : "",
        title: typeof schema.title === "string" ? schema.title : titleFromSchemaName(name),
        required: Array.isArray(schema.required) ? schema.required : [],
        properties: Object.keys(schema.properties ?? {}),
      };
    }),
  );
}

async function packageSummaries() {
  const packageRoot = path.join(root, "packages");
  const names = (await fs.readdir(packageRoot)).sort();
  const packages = [];
  for (const name of names) {
    const packagePath = path.join(packageRoot, name, "package.json");
    try {
      const pkg = JSON.parse(await fs.readFile(packagePath, "utf8"));
      packages.push({
        name: pkg.name,
        directory: `packages/${name}`,
        private: pkg.private === true,
        exports: pkg.exports,
        dependencies: Object.keys(pkg.dependencies ?? {}).filter((dependency) => dependency.startsWith("@openwiki/")),
      });
    } catch (error) {
      if (!isEnoent(error)) {
        throw error;
      }
    }
  }
  return packages;
}

