async function writeReferenceDocs(docs) {
  for (const [relativePath, content] of docs.entries()) {
    const target = path.join(root, relativePath);
    await fs.mkdir(path.dirname(target), { recursive: true });
    await fs.writeFile(target, content);
  }
}

async function checkReferenceDocs(docs) {
  const mismatches = [];
  for (const [relativePath, expected] of docs.entries()) {
    const actual = await fs.readFile(path.join(root, relativePath), "utf8").catch(() => "");
    if (actual !== expected) {
      mismatches.push(relativePath);
    }
  }
  if (mismatches.length > 0) {
    console.error("Reference docs are out of date. Run `pnpm docs:reference`.");
    for (const mismatch of mismatches) {
      console.error(`- ${mismatch}`);
    }
    process.exitCode = 1;
  }
}

function cliUsageLines(help) {
  return help
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line.startsWith("openwiki "));
}

function renderMcpToolRow(name, tool) {
  if (!tool) {
    throw new Error(`Missing MCP tool definition for ${name}`);
  }
  const schema = isRecord(tool.inputSchema) ? tool.inputSchema : {};
  const properties = isRecord(schema.properties) ? Object.keys(schema.properties) : [];
  const required = Array.isArray(schema.required) ? schema.required : [];
  const optional = properties.filter((property) => !required.includes(property));
  const annotations = isRecord(tool.annotations) ? tool.annotations : {};
  return `| \`${name}\` | ${cell(String(tool.title ?? ""))} | ${required.map((field) => `\`${field}\``).join(", ") || "None"} | ${optional.map((field) => `\`${field}\``).join(", ") || "None"} | ${annotations.readOnlyHint === true ? "yes" : "no"} |`;
}

function titleFromSchemaName(name) {
  return name
    .replace(/\.schema\.json$/u, "")
    .split(/[-_]/u)
    .map((part) => part.slice(0, 1).toUpperCase() + part.slice(1))
    .join(" ");
}

function exportShape(exportsValue) {
  if (exportsValue === undefined) {
    return "none";
  }
  if (typeof exportsValue === "string") {
    return `\`${exportsValue}\``;
  }
  if (isRecord(exportsValue)) {
    return Object.keys(exportsValue).map((key) => `\`${key}\``).join(", ");
  }
  return "custom";
}

function titleCase(value) {
  return value.slice(0, 1).toUpperCase() + value.slice(1);
}

function cell(value) {
  return value.replace(/\|/gu, "\\|").replace(/\n+/gu, " ");
}

function isRecord(value) {
  return typeof value === "object" && value !== null;
}

function isEnoent(error) {
  return isRecord(error) && error.code === "ENOENT";
}
