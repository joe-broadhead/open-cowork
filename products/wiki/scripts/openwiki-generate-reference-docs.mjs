function renderErrorReference() {
  return `${GENERATED}# Error Model

OpenWiki errors are tagged for scripts and agents by category, HTTP status, CLI
exit behavior, and MCP JSON-RPC code. Older routes may still expose legacy
string codes such as \`bad_request\`, \`forbidden\`, \`runtime_busy\`, or
\`invalid_git_revision\`; consumers should prefer the category/status behavior
below when possible.

| Category | Canonical code | HTTP status | CLI exit | MCP JSON-RPC code | Retryable | Meaning |
| --- | --- | ---: | ---: | ---: | --- | --- |
${OPENWIKI_ERROR_MODEL.map(
  (entry) =>
    `| \`${entry.category}\` | \`${entry.code}\` | ${entry.http_status} | ${entry.cli_exit_code} | ${entry.mcp_jsonrpc_code} | ${entry.retryable ? "yes" : "no"} | ${cell(entry.description)} |`,
).join("\n")}

## Adapter Behavior

- CLI commands print \`openwiki: <message>\` to stderr and exit non-zero on
  errors. JSON-producing commands keep success payloads on stdout.
- HTTP API errors use the status in the table where the route can classify the
  failure. Error bodies contain an \`error\` object with at least a message and,
  for policy and validation helpers, a code.
- MCP tool failures map authorization to \`-32001\`, invalid requests to JSON-RPC
  validation errors, invalid Git revisions to \`-32602\`, and unclassified
  failures to internal JSON-RPC errors.
- Scripts and agents should branch on status/category first, then message text
  only for operator display.
`;
}

