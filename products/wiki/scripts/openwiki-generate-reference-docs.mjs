function renderMcpReference(mcp, manifest) {
  const allTools = mcp.write;
  const toolsByName = new Map(allTools.map((tool) => [tool.name, tool]));
  const sections = ["read", "proposal", "write"].map((mode) => {
    const operations = mcpToolModeOperations(mode);
    return `## ${titleCase(mode)} Mode Tools

Minimum mode: \`${mode}\`. A \`${mode}\` server exposes these tools${mode === "read" ? "" : " plus lower-mode tools"}.

| Tool | Title | Required input | Optional input | Read-only hint |
| --- | --- | --- | --- | --- |
${operations.map((name) => renderMcpToolRow(name, toolsByName.get(name))).join("\n")}`;
  });
  return `${GENERATED}# MCP Tools

OpenWiki MCP tools are generated from the live \`tools/list\` definitions and
grouped by the minimum mode that exposes each tool. A \`proposal\` endpoint also
includes all read tools. A \`write\` endpoint includes read, proposal, and write
tools. Tool mode controls exposure; policy scopes still authorize calls.

Remote HTTP clients can discover the MCP endpoint from \`mcp-manifest.json\`.
The HTTP endpoint is \`${manifest.http_endpoint}\` and supports MCP Streamable
HTTP with protocol version \`${manifest.http_transport.protocol_version}\`.

Tool results are bounded. The default final output ceiling is
\`${manifest.tool_output.default_max_bytes}\` bytes and can be changed with
\`${manifest.tool_output.environment_variable}\`. Oversized tool calls return a
truncation envelope with preview JSON and guidance.

| Endpoint mode | Exposed tool count |
| --- | ---: |
| \`read\` | ${mcp.read.length} |
| \`proposal\` | ${mcp.proposal.length} |
| \`write\` | ${mcp.write.length} |

${sections.join("\n\n")}
`;
}

function renderOperationsReference(mcp, openapi, manifest) {
  const publicOperations = operationNames().filter((operation) => operation !== "wiki.admin");
  const openApiPathCount = Object.keys(openapi.paths ?? {}).length;
  const modeByOperation = new Map();
  for (const mode of ["read", "proposal", "write"]) {
    for (const operation of mcpToolModeOperations(mode)) {
      modeByOperation.set(operation, mode);
    }
  }
  return `${GENERATED}# Operation Contracts

OpenWiki operations are the stable vocabulary shared by CLI, HTTP, MCP,
OpenAPI, capabilities, static export, and policy. This file is generated from
the policy operation inventory and checked by \`tests/operation-contracts.test.ts\`.

Current generated contract evidence:

- public operations: ${publicOperations.length}
- OpenAPI paths: ${openApiPathCount}
- MCP read-mode tools: ${mcp.read.length}
- MCP proposal-mode tools: ${mcp.proposal.length}
- MCP write-mode tools: ${mcp.write.length}
- MCP manifest modes: ${Object.keys(manifest.tool_modes ?? {}).join(", ")}

When adding an operation, update the policy operation list, implementation
surface, generated OpenAPI document, MCP manifest when applicable, CLI help when
applicable, and operation parity tests.

| Operation | Required scopes | MCP minimum mode |
| --- | --- | --- |
${publicOperations
  .map((operation) => `| \`${operation}\` | ${requiredScopesForOperation(operation).map((scope) => `\`${scope}\``).join(", ")} | ${modeByOperation.has(operation) ? `\`${modeByOperation.get(operation)}\`` : "HTTP/CLI only"} |`)
  .join("\n")}
`;
}

