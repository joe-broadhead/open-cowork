function alpha(value: number) {
  return value.toFixed(3).replace(/0+$/, '').replace(/\.$/, '')
}

function parseHexRgb(value: string) {
  const match = value.match(/^#([0-9a-f]{3}|[0-9a-f]{6})$/i)
  if (!match) return null
  const hex = match[1]
  if (!hex) return null
  const pairs = hex.length === 3
    ? hex.split('').map((part) => part + part)
    : hex.match(/[0-9a-f]{2}/gi)
  if (!pairs || pairs.length !== 3) return null
  return pairs.map((part) => Number.parseInt(part, 16)) as [number, number, number]
}

function strengthenRgba(value: string, minimum: number) {
  return value.replace(
    /rgba\(\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*\)/g,
    (_match, red: string, green: string, blue: string, rawAlpha: string) => {
      const next = Math.max(Number.parseFloat(rawAlpha), minimum)
      return `rgba(${red}, ${green}, ${blue}, ${alpha(next)})`
    },
  )
}

function alphaColor(value: string, minimum: number) {
  const rgba = value.match(/^rgba\(\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*\)$/i)
  if (rgba) {
    const [, red, green, blue, rawAlpha] = rgba
    const current = Number.parseFloat(rawAlpha || '1')
    const next = current >= minimum ? Math.min(1, current + 0.08) : minimum
    return `rgba(${red}, ${green}, ${blue}, ${alpha(next)})`
  }

  const rgb = value.match(/^rgb\(\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*\)$/i)
  if (rgb) {
    const [, red, green, blue] = rgb
    return `rgba(${red}, ${green}, ${blue}, ${alpha(minimum)})`
  }

  const hex = parseHexRgb(value)
  if (hex) {
    const [red, green, blue] = hex
    return `rgba(${red}, ${green}, ${blue}, ${alpha(minimum)})`
  }

  return undefined
}

function deriveBorderStrong(tokens: ThemeTokenSeed, scheme: ResolvedColorScheme) {
  if (tokens.borderStrong) return tokens.borderStrong
  const targetAlpha = scheme === 'dark' ? 0.18 : 0.18
  const alphaBorder = alphaColor(tokens.border, targetAlpha)
  if (alphaBorder) return alphaBorder
  return `color-mix(in srgb, ${tokens.border} 72%, ${tokens.text} 28%)`
}

function softenGradient(value: string) {
  if (value === GRADIENT_NONE) return value
  return value.replace(
    /rgba\(\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*,\s*([0-9.]+)\s*\)/g,
    (_match, red: string, green: string, blue: string, rawAlpha: string) => {
      const current = Number.parseFloat(rawAlpha)
      const next = Number.isFinite(current) ? Math.max(0.025, Math.min(current * 0.625, 0.08)) : current
      return `rgba(${red}, ${green}, ${blue}, ${alpha(next)})`
    },
  )
}

export function refineThemeTokens(tokens: ThemeTokenSeed, scheme: ResolvedColorScheme): ThemeTokens {
  const dark = scheme === 'dark'
  return {
    ...tokens,
    accent2: tokens.accent2 || tokens.accentHover,
    accentText: accentTextForBackground(tokens.accent, tokens.accent2 || tokens.accentHover, tokens.base),
    accentActionForeground: accentActionForegroundForColors(tokens.accent, tokens.accent2 || tokens.accentHover),
    accentSoft: tokens.accentSoft || accentSoftToken(),
    accentLine: tokens.accentLine || accentLineToken(),
    accentForeground: tokens.accentForeground || accentForegroundForColor(tokens.accent),
    surface: strengthenRgba(tokens.surface, dark ? 0.055 : 0.04),
    surfaceHover: strengthenRgba(tokens.surfaceHover, dark ? 0.10 : 0.08),
    surfaceActive: strengthenRgba(tokens.surfaceActive, dark ? 0.17 : 0.13),
    border: strengthenRgba(tokens.border, dark ? 0.12 : 0.12),
    borderSubtle: strengthenRgba(tokens.borderSubtle, dark ? 0.04 : 0.055),
    borderStrong: deriveBorderStrong(tokens, scheme),
    shadowCard: tokens.shadowCard,
    shadowElevated: tokens.shadowElevated,
    bgImage: softenGradient(tokens.bgImage),
  }
}

export function applyThemeAccent(tokens: ThemeTokens, accentId: UiAccentPresetId = DEFAULT_UI_ACCENT_PRESET_ID): ThemeTokens {
  const next = applyDesignAccentTokens(tokens, accentId)
  return {
    ...next,
    accentText: accentTextForBackground(next.accent, next.accent2, next.base),
    accentActionForeground: accentActionForegroundForColors(next.accent, next.accent2),
    borderStrong: tokens.borderStrong,
  }
}

function refineThemeDefinition(theme: ThemeDefinitionSeed): ThemeDefinition {
  return {
    ...theme,
    dark: refineThemeTokens(theme.dark, 'dark'),
    light: refineThemeTokens(theme.light, 'light'),
  }
}

